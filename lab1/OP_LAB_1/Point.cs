namespace OP_LAB_1
{
    /// <summary>
    /// Point class
    /// </summary>
    public class Point
    {
        /// <summary>
        /// Point Coordinate
        /// </summary>
        /// Point constructor
        /// </summary>
        /// <param name="x">x coord</param>
        public int x, y;
        
        /// <summary>
        /// <param name="y">x coord</param>
        public Point(int x, int y)
        {
            this.x = x;
            this.y = y;
            
        }
    }
}